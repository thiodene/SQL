# CentOS/RHEL/Fedora/Scientific Linux version 4.x/5.x/6.x or older users

# The syntax is:

### To start ###
service mysqld start
# OR
/etc/init.d/mysqld start
 
### To stop ###
service mysqld stop
# OR
/etc/init.d/mysqld stop
 
### To restart ###
service mysqld restart
# OR
/etc/init.d/mysqld restart
